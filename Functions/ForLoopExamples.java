public class ForLoopExamples {
    public static void main(String[] args) {
    
    for (int num = 10; num >= 1; num--) {
        System.out.println(num);
    }
    int i = 2;
int count = 0;
while (count < 10) {
    System.out.println(i);
    i += 2;
    count++;
} 
    }
}





